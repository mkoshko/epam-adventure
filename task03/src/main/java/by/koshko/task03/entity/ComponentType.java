package by.koshko.task03.entity;

/**
 * This enum contains types for {@code Component} objects.
 */
public enum ComponentType {
    /**
     * Indicates that the {@code Components} is a text.
     */
    TEXT,
    /**
     * Indicates that the {@code Components} is a paragraph.
     */
    PARAGRAPH,
    /**
     * Indicates that the {@code Components} is a sentence.
     */
    SENTENCE,
    /**
     * Indicates that the {@code Components} is a lexeme.
     */
    LEXEME,
    /**
     * Indicates that the {@code Components} is a word.
     */
    WORD,
    /**
     * Indicates that the {@code Components} is a mark.
     */
    MARK,
    /**
     * Indicates that the {@code Components} is a symbol.
     */
    SYMBOL
}
